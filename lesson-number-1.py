# 4. Пользователь вводит целое положительное число. Найдите
# самую большую цифру в числе.
# Для решения используйте цикл while и арифметические операции.
answer = int(input('Введите целое положительное число: '))
max = 0
while answer > 0:
    number = answer % 10
    answer = answer // 10
    if number > max:
        max = number
print(f"Самая большая цифра в числе: {max}")

