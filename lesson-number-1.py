# 6. Спортсмен занимается ежедневными пробежками.
# В первый день его результат составил a километров.
# Каждый день спортсмен увеличивал результат
# на 10 % относительно предыдущего. Требуется определить номер дня,
# на который общий результат спортсмена составить не менее b километров.
# Программа должна принимать значения параметров a и b
# и выводить одно натуральное число — номер дня.

a = int(input('Результат пробежки в 1 день(км): '))
b = int(input('Желаемый результат ежедневных пробежек(км): '))
max = 1
while a < b:
    max += 1
    a *= 1.1
print(f"День, на который общий результат составит не менее {b} км: {max}")
